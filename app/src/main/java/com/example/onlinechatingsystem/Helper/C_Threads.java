package com.example.onlinechatingsystem.Helper;

public class C_Threads {


    private int id;
    private String name,email,chat_table,msg_table;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getChat_table() {
        return chat_table;
    }

    public void setChat_table(String chat_table) {
        this.chat_table = chat_table;
    }

    public String getMsg_table() {
        return msg_table;
    }

    public void setMsg_table(String msg_table) {
        this.msg_table = msg_table;
    }
}
